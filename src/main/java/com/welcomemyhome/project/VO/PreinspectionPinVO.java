package com.welcomemyhome.project.VO;

public class PreinspectionPinVO {
	private int pin_idx;
	private int pin_x;
	private int pin_y;
	private int pin_type;
	private String pin_picture_path;
	private String pin_content;
	private int preinspection_idx;
	
	public int getPin_idx() {
		return pin_idx;
	}
	public void setPin_idx(int pin_idx) {
		this.pin_idx = pin_idx;
	}
	public int getPin_x() {
		return pin_x;
	}
	public void setPin_x(int pin_x) {
		this.pin_x = pin_x;
	}
	public int getPin_y() {
		return pin_y;
	}
	public void setPin_y(int pin_y) {
		this.pin_y = pin_y;
	}
	public int getPin_type() {
		return pin_type;
	}
	public void setPin_type(int pin_type) {
		this.pin_type = pin_type;
	}
	public String getPin_picture_path() {
		return pin_picture_path;
	}
	public void setPin_picture_path(String pin_picture_path) {
		this.pin_picture_path = pin_picture_path;
	}
	public String getPin_content() {
		return pin_content;
	}
	public void setPin_content(String pin_content) {
		this.pin_content = pin_content;
	}
	public int getPreinspection_idx() {
		return preinspection_idx;
	}
	public void setPreinspection_idx(int preinspection_idx) {
		this.preinspection_idx = preinspection_idx;
	}
}
